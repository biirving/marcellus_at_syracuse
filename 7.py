def part_two(t_map):
    start = t_map[0].index('S')
    cur_indicies= {}
    cur_indicies[start] = 1
    for x in range(2, len(t_map), 2):
        next_indices = {}
        for i in cur_indicies.keys():
            if t_map[x][i] == '^':
                # Split!
                if i+1 in next_indices.keys():
                    next_indices[i+1] += cur_indicies[i]
                else:
                    next_indices[i+1] = cur_indicies[i]

                if i-1 in next_indices.keys():
                    next_indices[i-1] += cur_indicies[i]
                else:
                    next_indices[i-1] = cur_indicies[i]
            else:
                if i in next_indices.keys():
                    next_indices[i] += cur_indicies[i]
                else:
                    next_indices[i] = cur_indicies[i]
        cur_indicies = next_indices
    total_paths = sum([v for k, v in cur_indicies.items()])
    return total_paths 

