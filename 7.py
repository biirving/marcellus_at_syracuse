t_map = input.split('\n')



def part_one(t_map):
    start = t_map[0].index('S')
    cur_indicies = [start]
    total_splits = 0
    for x in range(2, len(t_map), 2):
        next_indices = []
        for i in cur_indicies:
            if t_map[x][i] == '^':
                # Split!
                next_indices.append(i+1)
                next_indices.append(i-1)
                total_splits += 1
            else:
                next_indices.append(i)
        cur_indicies = list(set(next_indices))
    return total_splits

